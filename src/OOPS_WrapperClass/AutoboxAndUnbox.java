package OOPS_WrapperClass;

public class AutoboxAndUnbox {

    public static void main(String[] args) {

        //Autobox---Converting primitive to Wrapper

        int a=50;
        Integer in=a;

        System.out.println(in);
        //in is having so many methods

        //unboxing-- convert wrapper to primitive

        int a1=in;
        System.out.println(a1);
        //a1 dont have any methods


    }
}
